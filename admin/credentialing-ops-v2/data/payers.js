export function getPayerById(id) {
  return PAYER_CATALOG.find(p => p.id === id) || null;
}

export function getPayerByName(name) {
  return PAYER_CATALOG.find(p => p.name === name || p.shortName === name) || null;
}

export function getPayersByCategory(category) {
  return PAYER_CATALOG.filter(p => p.category === category);
}

export function getPayersByState(state) {
  return PAYER_CATALOG.filter(p =>
    p.states && (p.states.includes(state) || p.states.includes('ALL'))
  );
}

export function getPayersByRegion(region) {
  return PAYER_CATALOG.filter(p => p.region === region);
}

